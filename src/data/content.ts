/** Build WhatsApp / Viber / Telegram deep links from E.164-style number (e.g. +38975844462). */
export function buildMessagingLinks(phoneE164: string) {
  const digits = phoneE164.replace(/\D/g, '')
  const withPlus = phoneE164.startsWith('+') ? phoneE164 : `+${digits}`
  return {
    whatsapp: `https://wa.me/${digits}`,
    viber: `viber://chat?number=${encodeURIComponent(withPlus)}`,
    telegram: `https://t.me/${withPlus}`,
  }
}

